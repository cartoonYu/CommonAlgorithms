package DataStructure.Map;

public class InputMap {

    private int start;   //start int map

    private int end;    //end in map

    private int length;   //length between start and end

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
